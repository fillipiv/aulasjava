package exercicios;

public class Exercicio01 {
 public static void main(String[] args) {
     //programa para exibir o resultado das 4 operações matemáticas básicas entre os números 15 e 7
     System.out.println("Resultado da + é " + (15+7));
     System.out.println("Resultado da - é " + (15-7));
     System.out.println("Resultado da - é " + (7-15));
     System.out.println("Resultado da * é " + (15*7));
     System.out.println("Resultado da / é " + (15/7));
     System.out.println("Resultado da / é " + (7/15));
     //Respostas prof:
     System.out.println("15 + 7 = " + (15+7));
     System.out.println("15 - 7 = " + (15-7));
     System.out.println("15 * 7 = " + (15*7));
     System.out.println("15 / 7 = " + (15.0/7));
     // Se adiciono .0 a um número inteiro, ele entenderá como float e trará casas decimais. Sem o .0
     // vai entender como número inteiro e arredonda,
 }   

}
