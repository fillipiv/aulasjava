package exemplos;

public class Exemplo02 {
    public static void main(String[] args) {
        // operador '+' concatenando
        System.out.println("Resultado = " + 2 + 3);
          // operador '+' somando, pode ter comportamento diferente dependendo do operando
        System.out.println(2 + 3);
          // alterar ordem de prioridade, interpreta primeiramente o valor e não a string
          System.out.println("Resultado = " + (2 + 3));

          //operadores matemáticos (aritiméticos)
          // * / + -
          System.out.println(2 + 3 * 5);
    }
}
