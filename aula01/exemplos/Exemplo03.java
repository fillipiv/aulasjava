package exemplos;

public class Exemplo03 {
    public static void main(String[] args) {
        // SYNTAXE: TIPO NOME;
        int valor; // cria (declara) uma variável chamada valor, do tipo inteiro
        // linha amarela > warning: aviso de "algo estranho"
        valor = 10; // atribui o valor 10 para a variável 'valor'

        System.out.println(valor);
        //depois de adicionar o syso, a linha amarela desaparece pois a variável criada passa a ser utilizada e faz sentido
        
        valor = 5; // substituição. Uma variável armazena um único valor em cada momento

        System.out.println(valor);
    
    }
    
}
